
public class Job {
	private String role;
	private long salary;
	private int id;
	public void setRole(String role) {
		this.role = role;
	}
	public String getRole() {
		return role;
	}
	public void setSalary(long salary) {
		this.salary = salary;
	}
	public long getSalary() {
		return salary;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getId() {
		return id;
	}
}
